public class Quadrado 
{
	private double lado;
	
	public Quadrado(double lado) 
	{
		this.lado = lado;
	}//	Fim do construtor Quadrado

	public double calcularArea()
	{
		return lado * lado;
	}// Fim do método calcular area
}//	Fim da classe Quadrado
