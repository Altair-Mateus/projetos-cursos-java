
public class TesteComVariaveis 
{
	
	public static void main(String[] args) 
	{
		//Declaração com atribuição de valores separada
		int numero;
		
		numero = 12;
		
		//Alteração do valor da variável
		numero--;
		numero *= 2;
		
		System.out.println(numero);
		
		
		//Declaração da variável junto com o valor
		int num1 = 21;
		
		System.out.println(num1);
		
		
		//Soma com variáveis
		int num2 = num1 + 5;
		
		System.out.println(num2);
	}
}
