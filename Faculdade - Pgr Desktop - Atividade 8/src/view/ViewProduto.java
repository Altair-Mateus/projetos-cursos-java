    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed

        Produtos obj = new Produtos();

        obj.setNome(jTextNome.getText());
        obj.setPrecoCusto(Double.parseDouble(jTextPrecoCusto.getText()));
        obj.setPrecoVenda(Double.parseDouble(jTextPrecoVenda.getText()));
        obj.setEstoque(Integer.parseInt(jTextEstoque.getText()));
        obj.setNcm(Integer.parseInt(jTextNcm.getText()));
        obj.setUnMedida(jTextUnMedida.getText());
        obj.setMarca(jTextMarca.getText());
        obj.setModelo(jTextModelo.getText());
        obj.setOrigem(jTextOrigem.getText());
        obj.setCodBarras(Integer.parseInt(jTextCodBarras.getText()));
        obj.setSituacao(jTextSituacao.getText());
        obj.setCategoria(jTextCategoria.getText());
        obj.setInfoAdicionais(jTextInfoAdicionais.getText());

        ProdutosDao dao = new ProdutosDao();
        dao.cadProduto(obj);

        limpaCampos();
        
        try 
        {
            listarTabela();
        } catch (SQLException ex) 
        {
            Logger.getLogger(ViewProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

