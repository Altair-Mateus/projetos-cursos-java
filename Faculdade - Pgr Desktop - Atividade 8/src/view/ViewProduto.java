    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        
        String nome = "%" + jTextFieldPesquisa.getText() + "%";
        
        ProdutosDao dao = new ProdutosDao();
        List<Produtos> lista = dao.buscaCad(nome);
        
        DefaultTableModel dados = (DefaultTableModel) jTableProdutos.getModel();
        dados.setNumRows(0);
        
        for (Produtos p : lista)
        {
            dados.addRow(new Object[]
            {
                p.getCod(),
                            p.getNome(),
                            p.getPrecoCusto(),
                            p.getPrecoVenda(),
                            p.getIcms(),
                            p.getLucro(),
                            p.getEstoque(),
                            p.getNcm(),
                            p.getUnMedida(),
                            p.getMarca(),
                            p.getModelo(),
                            p.getOrigem(),
                            p.getCodBarras(),
                            p.getSituacao(),
                            p.getCategoria(),
                            p.getInfoAdicionais()
            }
            );
        }// Fim do bloco de repetição for
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

