
public class Aplicacao
{
	public static void main (String[] args)
	{
		/*Mudança no tipo de dado do 10 de int
		 para long (Casting implícito)*/
		long x = 10;
		
		/*Mudança no tipo de dado do 35 de int
		 para double (Casting implícito)*/
		double z = 35;
		
		//Exibindo os valores
		System.out.println(x);
		System.out.println(z);
		
		
		/*Mudança no tipo de dado do 40.0 de double
		 para float (Casting Explícito)*/
		float y = (float) 40.0;
		
		//Exibindo os valores
		System.out.println(y);
	}
}
